@Client.on_callback_query(filters.regex("flogo"))
async def logo_doc(_,query):
  await query.answer()
  try:
    x = await query.message.reply_text("`üîÑ Sending You The Logo As File`")
    await query.message.edit_reply_markup(reply_markup=None)
    link = "https://graph.org//file/" + query.data.replace("flogo","").strip() + ".jpg"
    await query.message.reply_document(link,caption="**üñº Logo Generated By @MEmpire_Offical**")
  except FloodWait:
    pass
  except Exception as e:
    try:
      return await x.edit(f"`‚ùå Something Went Wrong...`\n\nReport This Error In [support](https://t.me/MEmpire_Comment_Bot) \n\n`{str(e)}`")
    except:
      return
    
  return await x.delete()
